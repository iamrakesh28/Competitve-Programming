#include <iostream>
#include <cmath>

using namespace std;

const double PI = 3.141592653589;

int main() {
  int t;
  scanf("%d", &t);
  while (t--) {
    int n, side;
    scanf("%d", &n);
    side = n << 1;
    double angle = PI / side;
    angle = tan(angle);
    double ans = 1.0 / angle;
    printf("%0.10lf\n", ans);
  }
  return 0;
}
